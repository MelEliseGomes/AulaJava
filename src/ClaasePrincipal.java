
public class ClaasePrincipal {

	public static void main(String[] args) {

		// Printar mensagem pulando linha
		System.out.println("Pulando linha"); //sysout + space
		
		// Printar mensagem sem pular linha
		System.out.printf("Sem pular linha"); //sysout + space"
		
		System.out.println("Pulando linha"); //sysout + space
		
		//Sequência de scape
		//Usar aspas duplas
		System.out.println("Meu nome é \"Fulano\"");
		//Usar aspas simples
		System.out.println("Meu nome é \'Fulano\'");
		//Pular linha, quebrar linha
		System.out.println("Meu nome é \n Fulano");
	}

}
